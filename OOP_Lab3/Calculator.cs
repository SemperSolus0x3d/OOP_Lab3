using System;
using System.Linq;

namespace OOP_Lab3
{
    public class Calculator
    {
        // Сложение
        public decimal Add(decimal a, decimal b)
        {
            return a + b;
        }

        // Вычитание
        public decimal Substract(decimal a, decimal b)
        {
            return a - b;
        }

        // Умножение
        public decimal Multiply(decimal a, decimal b)
        {
            return a * b;
        }
    }
}
