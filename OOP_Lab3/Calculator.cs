using System;
using System.Collections.Generic;
using System.Text;

namespace OOP_Lab3
{
    public class Calculator
    {
        // Сложение
        private double Add(double a, double b)
        {
            return a + b;
        }

        // Вычитание
        private double Substract(double a, double b)
        {
            return a - b;
        }

        // Умножение
        private double Multiply(double a, double b)
        {
            return a * b;
        }
    }
}
